    class DetMovimientoTableModel extends AbstractTableModel
    {
          String [] nombreColumna = {"Id", "producto","cantidad"};
        
        @Override
        public int getRowCount() {
            return detalles.size();
        }

        @Override
        public int getColumnCount() {
            return nombreColumna.length;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            DetalleMovimiento det=detalles.get(rowIndex);
            String valor=null;
            switch(rowIndex)
            {
                case 0:
                    valor=""+det.getArticulo().getId();
                    break;
                case 1:
                    valor=det.getArticulo().getNombre();
                    break;
                case 2:
                    valor=""+det.getCantidad();
                    break;
            }
            return valor;
        }
    }
    
