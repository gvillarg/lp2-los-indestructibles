    class GuiaRemisionTableModel extends AbstractTableModel{
        public ArrayList<GuiaRemision> guiasremision=Main.servicioGuiaRemision.getGuiasRemision();
        String [] nombreColumna = {"Id guía","Fecha", "monto"};
        
        @Override
        public int getRowCount() {
            return guiasremision.size();
        }
        
        @Override
        public int getColumnCount() {
            return 3;
        }
        
        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            GuiaRemision guiaremision=guiasremision.get(rowIndex);
            switch(columnIndex){
                case 0: return "" + guiaremision.getId();
                case 1: return guiaremision.getFecha();
                case 2: return guiaremision.getTotal();
            }
            return null;
        }
        @Override
        public String getColumnName(int columna){
            return nombreColumna[columna];
        }
    }
    
