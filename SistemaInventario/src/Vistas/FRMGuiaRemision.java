    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        GuiaRemision grem=new GuiaRemision();
        grem.setId(Integer.parseInt(txId.getText()));
        java.util.Date fecha=new java.util.Date(txFecha.getText());
        grem.setFecha(fecha);
        grem.setTotal(Double.parseDouble(txMonto.getText()));
        
        Main.servicioGuiaRemision.agregarGuiaRemision(grem);
        actualizarTabla();
    }//GEN-LAST:event_btnAceptarActionPerformed
    
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (idSeleccionado!=0){
            GuiaRemision grem=new GuiaRemision();
            grem.setId(idSeleccionado);
            java.util.Date fecha=new java.util.Date(txFecha.getText());
            grem.setFecha(fecha);
            grem.setTotal(Double.parseDouble(txMonto.getText()));
            
            Main.servicioGuiaRemision.editarGuiaRemision(grem);
            actualizarTabla();
        }
    }//GEN-LAST:event_btnEditarActionPerformed
    
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (idSeleccionado!=0){
            Main.servicioGuiaRemision.eliminaGuiaRemision(idSeleccionado);
            actualizarTabla();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
    
    public void actualizarTabla(){
        grTableModel.guiasremision = Main.servicioGuiaRemision.getGuiasRemision();
        grTableModel.fireTableChanged(null);
    }
